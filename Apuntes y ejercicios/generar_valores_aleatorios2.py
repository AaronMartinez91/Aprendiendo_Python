# Valores aleatorios con funciones (o métodos) randint (random integer)

# Importamos todo el módulo random, esto me permite usar toda su biblioteca
import random

# Generar un int aleatorio entre 1 y 10 (ambos incluidos) con el método:
numero = random.randint(1,10) # En este caso antes del randint debo especificar el módulo con un punto
print(f"Número aleatorio generado: {numero}")


